// integers.cpp
#include <iostream>
using namespace std;

int main() {
   char ch = 65;
   short s = -123;
   int i = 456;
   long l = 123456L;
   long long ll = 9876543210LL;

   cout << "char: " << ch << endl;
   cout << "short: " << s << endl;
   cout << "int: " << i << endl;
   cout << "long: " << l << endl;
   cout << "long long: " << ll << endl;
   return 0;
}